const isValidSudoku = (board) => {
    for (const arr of board) {
        let xMap = {}
        for (let num of arr) {
            if (!xMap[num] && num != ".") {
                xMap[num] = 1
            } else if (xMap[num] && num != ".") {
                return false
            }
        }
    }
}

